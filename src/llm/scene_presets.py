def genre_key(genre: str | None) -> str:
    if genre in GENRE_SCENE_PRESETS:
        return str(genre)
    return COMMON_GENRE_KEY


def presets_for_genre(genre: str | None) -> list[dict[str, str]]:
    return GENRE_SCENE_PRESETS.get(str(genre), COMMON_SCENE_PRESETS)


def scene_preset_labels(genre: str | None) -> list[str]:
    return [preset["label"] for preset in presets_for_genre(genre)]


def resolve_scene_preset(
    genre: str | None,
    preset_label: str | None = None,
    sample_index: int = 1,
    bucket_count: int | None = None,
) -> dict[str, str]:
    presets = presets_for_genre(genre)
    selected_presets = presets[: bucket_count or len(presets)]
    if not selected_presets:
        selected_presets = COMMON_SCENE_PRESETS

    if preset_label and preset_label != AUTO_SCENE_PRESET:
        for preset in presets:
            if preset_label in {preset["label"], preset["id"]}:
                return {**preset, "genre_key": genre_key(genre)}

    preset = selected_presets[(sample_index - 1) % len(selected_presets)]
    return {**preset, "genre_key": genre_key(genre)}


